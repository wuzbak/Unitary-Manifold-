# Chiral zero mode wavefunction
# ---------------------------------------------------------------------------

def chiral_zero_mode_wavefunction(
    y_grid: np.ndarray,
    phi0: float = PHI_0,
    sector: str = "left",
) -> np.ndarray:
    """Wavefunction of the chiral zero mode on the orbifold interval [0, πR].

    For the massless chiral zero mode, the wavefunction depends on the
    radion background φ₀ which sets the compactification radius R = φ₀:

        Left sector:   ψ_L(y) ∝ exp(−α y / (πR))   (localised at y=0)
        Right sector:  ψ_R(y) ∝ exp(+α y / (πR))   (localised at y=πR)

    where α = φ₀ / (2π) is the localisation exponent derived from the
    Goldberger–Wise radion potential.  For φ₀ = n_w × 2π = 10π, α = 5.

    The wavefunction is normalised over [0, πR]:
        ∫₀^{πR} |ψ(y)|² dy = 1

    Parameters
    ----------
    y_grid : ndarray, shape (M,) — coordinate along fifth dimension [0, πR]
    phi0   : float — radion vacuum value (default: N_W × 2π)
    sector : str   — 'left' or 'right' (default: 'left')

    Returns
    -------
    psi : ndarray, shape (M,) — real, normalised zero mode wavefunction

    Raises
    ------
    ValueError
        If sector is not 'left' or 'right'.
    """
    if sector not in ("left", "right"):
        raise ValueError(f"sector must be 'left' or 'right', got {sector!r}")
    if phi0 <= 0.0:
        raise ValueError(f"phi0 must be positive, got {phi0!r}")

    y = np.asarray(y_grid, dtype=float)
    R = phi0                      # compactification radius = φ₀ in Planck units
    pi_R = math.pi * R            # interval length

    # Localisation exponent: α = φ₀ / (2π)
    alpha = phi0 / (2.0 * math.pi)

    if sector == "left":
        raw = np.exp(-alpha * y / (pi_R + _EPS))
    else:  # right
        raw = np.exp(+alpha * y / (pi_R + _EPS))

    # Normalise: ∫₀^{πR} |ψ|² dy = 1  using trapezoidal rule
    norm_sq = np.trapezoid(raw**2, y)
    psi = raw / math.sqrt(norm_sq + _EPS)
    return psi


