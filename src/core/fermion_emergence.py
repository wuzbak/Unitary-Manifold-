# 4D Dirac gamma matrices (Dirac basis)
# ---------------------------------------------------------------------------

def gamma_matrices_4d() -> np.ndarray:
    """Return the 4D Dirac gamma matrices in the Dirac (standard) basis.

    γ⁰ = [[I₂,  0 ],     γ^k = [[0,   σ^k],
           [0,  -I₂]]            [-σ^k, 0  ]]

    where σ^k are the Pauli matrices and k = 1, 2, 3.

    The Clifford algebra {γ^μ, γ^ν} = 2 η^{μν} is satisfied with
    η = diag(−1, +1, +1, +1).

    Returns
    -------
    gamma : ndarray, shape (4, 4, 4)
        gamma[mu] is the 4×4 matrix for index μ.
    """
    I2 = np.eye(2, dtype=complex)
    O2 = np.zeros((2, 2), dtype=complex)

    sigma_x = np.array([[0, 1], [1, 0]], dtype=complex)
    sigma_y = np.array([[0, -1j], [1j, 0]], dtype=complex)
    sigma_z = np.array([[1, 0], [0, -1]], dtype=complex)
    sigmas = [sigma_x, sigma_y, sigma_z]

    # γ⁰
    g0 = np.block([[I2, O2], [O2, -I2]])

    # γ^k for k=1,2,3
    gk = []
    for s in sigmas:
        gk.append(np.block([[O2, s], [-s, O2]]))

    gamma = np.array([g0] + gk)   # shape (4, 4, 4)
    return gamma


def gamma5_matrix() -> np.ndarray:
    """Return the 4D chirality matrix γ⁵ = i γ⁰ γ¹ γ² γ³.

    In the Dirac basis:
        γ⁵ = [[0, I₂],
               [I₂, 0]]

    Properties: (γ⁵)² = I, {γ⁵, γ^μ} = 0, hermitian.

    Returns
    -------
    g5 : ndarray, shape (4, 4)
    """
    I2 = np.eye(2, dtype=complex)
    O2 = np.zeros((2, 2), dtype=complex)
    return np.block([[O2, I2], [I2, O2]])


# ---------------------------------------------------------------------------
# 5D Gamma matrices
# ---------------------------------------------------------------------------

def gamma_matrices_5d() -> np.ndarray:
    """Return the five 5D Gamma matrices in an 8×8 representation.

    We use the embedding:
        Γ^μ = γ^μ ⊗ σ₁    (μ = 0,1,2,3)
        Γ^5 = I₄ ⊗ iσ₂

    where γ^μ are the 4D Dirac matrices (4×4), σ₁ and σ₂ are Pauli matrices,
    and ⊗ denotes the Kronecker (tensor) product.

    The 5D Clifford algebra {Γ^A, Γ^B} = 2 η^{AB} is satisfied for
    η = diag(−1,+1,+1,+1,+1).

    Returns
    -------
    Gamma : ndarray, shape (5, 8, 8)
        Gamma[A] is the 8×8 matrix for the A-th Gamma matrix.
    """
    gamma4 = gamma_matrices_4d()           # (4, 4, 4)
    I4 = np.eye(4, dtype=complex)

    sigma1 = np.array([[0, 1], [1, 0]], dtype=complex)
    sigma2 = np.array([[0, -1j], [1j, 0]], dtype=complex)

    Gamma = []
    for mu in range(4):
        Gamma.append(np.kron(gamma4[mu], sigma1))   # 4D matrices ⊗ σ₁

    # Γ⁵ = I₄ ⊗ (iσ₂)
    Gamma.append(np.kron(I4, 1j * sigma2))

    return np.array(Gamma)   # shape (5, 8, 8)


# ---------------------------------------------------------------------------
# Orbifold Z₂ projectors
# ---------------------------------------------------------------------------

def orbifold_z2_projector(parity: int) -> np.ndarray:
    """Return the Z₂ orbifold projector P_{±} = (1 ± γ⁵) / 2.

    The Z₂ action y → −y on the spinor is accompanied by:
        Ψ → +γ⁵ Ψ   (η = +1, even parity — left-handed sector)
        Ψ → −γ⁵ Ψ   (η = −1, odd parity  — right-handed sector)

    Parameters
    ----------
    parity : int — must be +1 (left-handed, Z₂-even) or −1 (right-handed)

    Returns
    -------
    P : ndarray, shape (4, 4) — projection matrix

    Raises
    ------
    ValueError
        If parity is not ±1.
    """
    if parity not in (+1, -1):
        raise ValueError(f"parity must be +1 or -1, got {parity!r}")
    g5 = gamma5_matrix()
    I4 = np.eye(4, dtype=complex)
    return 0.5 * (I4 + parity * g5)


