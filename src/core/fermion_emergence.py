def dirac_kk_spectrum(
    n_w: int = N_W,
    phi0: float = PHI_0,
    n_modes: int = 6,
) -> Dict[str, list]:
    """Compute the KK fermion mass spectrum on S¹/Z₂.

    The 5D Dirac spinor on the interval [0, πR] (where R = φ₀ in Planck
    units) expands in KK modes with masses:

        Untwisted left  (L): m_n^{(L)} = n / φ₀      n = 0, 1, 2, ...
        Untwisted right (R): m_n^{(R)} = n / φ₀      n = 1, 2, 3, ...
        Twisted (tw):        m_n^{(tw)} = (n+½) / φ₀  n = 0, 1, 2, ...

    The n=0 left mode is the massless chiral zero mode (SM fermion).
    Right zero mode is absent (Z₂ projection removes it).

    Parameters
    ----------
    n_w    : int   — winding number (for documentation; not used in mass formula)
    phi0   : float — radion vacuum value (= compactification radius in Planck units)
    n_modes: int   — number of KK levels to compute (default 6)

    Returns
    -------
    dict with keys:
        'untwisted_L' : list of float — left-handed KK masses m_n = n/φ₀
        'untwisted_R' : list of float — right-handed KK masses m_n = n/φ₀
        'twisted'     : list of float — twisted sector masses m_n = (n+½)/φ₀

    Raises
    ------
    ValueError
        If phi0 ≤ 0 or n_modes < 1.
    """
    if phi0 <= 0.0:
        raise ValueError(f"phi0 must be positive, got {phi0!r}")
    if n_modes < 1:
        raise ValueError(f"n_modes must be ≥ 1, got {n_modes!r}")

    untwisted_L = [n / phi0 for n in range(n_modes)]
    untwisted_R = [n / phi0 for n in range(1, n_modes + 1)]
    twisted = [(n + 0.5) / phi0 for n in range(n_modes)]

    return {
        "untwisted_L": untwisted_L,
        "untwisted_R": untwisted_R,
        "twisted": twisted,
        "n_w": n_w,
        "phi0": phi0,
    }


