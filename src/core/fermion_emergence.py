# ---------------------------------------------------------------------------
# Chirality eigenvalue of KK modes
# ---------------------------------------------------------------------------

def kk_mode_chirality(n: int, z2_parity: int = +1) -> str:
    """Return the chirality of the n-th KK fermion mode on S¹/Z₂.

    Z₂-even (parity +1) modes:
        n = 0  → massless left-handed   (SM fermion zero mode)
        n ≥ 1  → massive Dirac pair (L+R combination)

    Z₂-odd (parity -1) modes:
        all massive (no zero mode)

    Parameters
    ----------
    n         : int — KK mode index (n ≥ 0)
    z2_parity : int — +1 (even) or -1 (odd)

    Returns
    -------
    str — 'left-chiral', 'right-chiral', or 'massive-dirac'
    """
    if n < 0:
        raise ValueError(f"n must be ≥ 0, got {n!r}")
    if z2_parity not in (+1, -1):
        raise ValueError(f"z2_parity must be ±1, got {z2_parity!r}")

    if z2_parity == +1 and n == 0:
        return "left-chiral"
    elif z2_parity == -1 and n == 0:
        return "right-chiral-projected-out"
    else:
        return "massive-dirac"


# ---------------------------------------------------------------------------
# Full fermion emergence summary
# ---------------------------------------------------------------------------

def fermion_emergence_summary(n_w: int = N_W) -> Dict:
    """Return a complete summary of the fermion emergence from the orbifold.

    Parameters
    ----------
    n_w : int — winding number

    Returns
    -------
    dict with keys:
        'index_theorem'      : dict — Atiyah-Singer index check
        'sm_generations'     : list — SM generation identification
        'kk_spectrum'        : dict — KK mass spectrum
        'orbifold_sectors'   : list — sector table
        'fermion_condensate' : float — ⟨ψ̄ψ⟩ at background φ₀
        'twisted_mass_gap'   : float — lightest twisted sector mass
        'chiral_symmetry_breaking' : bool — condensate < 0 signals CSB
    """
    phi0 = n_w * 2.0 * math.pi
    phi_profile = np.full(10, phi0)

    idx = index_theorem_check(n_w)
    gens = sm_generation_identification(n_w)
    spectrum = dirac_kk_spectrum(n_w, phi0)
    sectors = orbifold_sector_table()
    cond = fermion_condensate(phi_profile, phi0)
    twist_gap = twisted_sector_mass_shift(phi0, n_twist=0)

    return {
        "n_w": n_w,
        "index_theorem": idx,
        "sm_generations": gens,
        "kk_spectrum": spectrum,
        "orbifold_sectors": sectors,
        "fermion_condensate": cond,
        "twisted_mass_gap": twist_gap,
        "chiral_symmetry_breaking": bool(cond < 0.0),
    }
