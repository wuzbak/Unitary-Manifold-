# Twisted sector mass shift
# ---------------------------------------------------------------------------

def twisted_sector_mass_shift(
    phi0: float = PHI_0,
    n_twist: int = 0,
) -> float:
    """Mass of the n-th twisted-sector fermion on S¹/Z₂.

    Twisted sector fields satisfy anti-periodic boundary conditions:
        Ψ(x, y + 2πR) = −Ψ(x, y)

    This shifts the KK mass levels by half a unit:
        m_n^{(tw)} = (n + ½) / R = (n + ½) / φ₀

    Parameters
    ----------
    phi0    : float — radion vacuum value (= R in Planck units)
    n_twist : int   — twisted KK level (n ≥ 0)

    Returns
    -------
    float — twisted sector mass

    Raises
    ------
    ValueError
        If phi0 ≤ 0 or n_twist < 0.
    """
    if phi0 <= 0.0:
        raise ValueError(f"phi0 must be positive, got {phi0!r}")
    if n_twist < 0:
        raise ValueError(f"n_twist must be ≥ 0, got {n_twist!r}")
    return (n_twist + 0.5) / phi0


# ---------------------------------------------------------------------------
# Dirac determinant ratio
# ---------------------------------------------------------------------------

def dirac_determinant_ratio(
    phi_profile: np.ndarray,
    phi0: float = PHI_0,
    n_modes: int = 10,
) -> float:
    """Ratio det(D₅(φ)) / det(D₅(φ₀)) via zeta-function regularisation.

    The fermion determinant is

        det(D₅(φ)) = ∏_n m_n(φ)

    where m_n(φ) = n / φ̄ with φ̄ = ⟨φ⟩.  The ratio to the background value
    is computed using the product over the first n_modes levels.

    This ratio detects topological phase transitions: it changes sign when
    the radion crosses a nodal point, signalling a change in the fermion
    spectrum topology.

    Parameters
    ----------
    phi_profile : ndarray — radion scalar field (1D array)
    phi0        : float   — background (reference) radion value
    n_modes     : int     — number of KK modes to include

    Returns
    -------
    float — determinant ratio det(D₅(φ)) / det(D₅(φ₀))
    """
    phi_profile = np.asarray(phi_profile, dtype=float)
    phi_mean = float(np.mean(np.abs(phi_profile)) + _EPS)

    # Product of m_n = n/φ for n=1,...,n_modes  (skip n=0 zero mode)
    masses_phi = [n / phi_mean for n in range(1, n_modes + 1)]
    masses_phi0 = [n / phi0 for n in range(1, n_modes + 1)]

    log_det_ratio = sum(
        math.log(m / m0 + _EPS) for m, m0 in zip(masses_phi, masses_phi0)
    )
    return math.exp(log_det_ratio)


