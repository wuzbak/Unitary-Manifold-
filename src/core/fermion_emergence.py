def sm_generation_identification(n_w: int = N_W) -> List[Dict]:
    """Map orbifold KK modes to Standard Model generations.

    The three stable KK modes (n=0, 1, 2) from Pillar 42 are identified
    with the three SM generations:

        n=0  →  Generation 1: electron, u-quark, d-quark   (lightest)
        n=1  →  Generation 2: muon,     c-quark, s-quark   (middle)
        n=2  →  Generation 3: tau,      t-quark, b-quark   (heaviest)

    Each generation has a left-handed doublet (Z₂-even zero mode) and
    a right-handed singlet (first non-zero KK mode at mass m_1 = 1/φ₀).

    Parameters
    ----------
    n_w : int — winding number (default: 5)

    Returns
    -------
    list of dict, one per stable generation:
        'mode_n'        : int   — KK mode index
        'generation'    : int   — SM generation number (1, 2, 3)
        'mass_ratio'    : float — m_n / m_0 = √(1 + n²/n_w)
        'chiral_partner': str   — 'left-zero-mode' or 'massive-KK-mode'
        'sm_particles'  : list  — particle names
        'phi_eff'       : float — effective radion φ_eff = φ₀ / √(1 + n²/n_w)
        'localisation'  : str   — which boundary the mode is localised at
    """
    stable_modes = [n for n in range(n_w + 1) if n * n <= n_w]
    if len(stable_modes) < 3:
        raise ValueError(
            f"n_w={n_w} gives only {len(stable_modes)} stable mode(s); "
            "need at least 3 for SM generation identification"
        )

    sm_particles = [
        ["electron", "electron-neutrino", "u-quark", "d-quark"],
        ["muon", "muon-neutrino", "c-quark", "s-quark"],
        ["tau", "tau-neutrino", "t-quark", "b-quark"],
    ]
    localisations = [
        "y=0 fixed point (UV brane)",
        "intermediate KK level",
        "y=πR fixed point (IR brane) adjacent",
    ]

    result = []
    for i, n in enumerate(stable_modes[:3]):
        mass_ratio = math.sqrt(1.0 + n * n / n_w)
        phi_eff = PHI_0 / mass_ratio
        result.append({
            "mode_n": n,
            "generation": i + 1,
            "mass_ratio": mass_ratio,
            "chiral_partner": "left-zero-mode" if n == 0 else "massive-KK-mode",
            "sm_particles": sm_particles[i],
            "phi_eff": phi_eff,
            "localisation": localisations[i],
        })

    return result


