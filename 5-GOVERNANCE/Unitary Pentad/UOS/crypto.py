# ===========================================================================
# GeometricHash
# ===========================================================================

class GeometricHash:
    """Manifold-native hash function.

    Produces a 32-byte geometric digest that encodes both the cryptographic
    content hash (via SHA-256) and the manifold structure (via the φ-
    fingerprint and winding projection).

    The output has two components:
      * Bytes 0–15: standard SHA-256 prefix (for collision resistance)
      * Bytes 16–31: manifold projection (for geometric proximity)

    Examples
    --------
    >>> gh = GeometricHash(b"hello manifold")
    >>> len(gh.digest())
    32
    >>> gh.phi_fingerprint()
    0.xxx  # in [0, 1)
    >>> gh.winding_class()
    int  # in [0, WINDING_NUMBER)
    """

    def __init__(self, data: bytes) -> None:
        self._data = data
        self._sha = hashlib.sha256(data).digest()
        self._phi = self._compute_phi(data)
        self._winding = self._compute_winding(data)

    def digest(self) -> bytes:
        """Return the 32-byte geometric digest."""
        # First 16 bytes from SHA-256; last 16 from manifold projection
        sha_half = self._sha[:16]
        phi_int = int(self._phi * (2 ** 64)) & 0xFFFFFFFFFFFFFFFF
        w_int = self._winding
        manifold_half = struct.pack(">QQ", phi_int, w_int * K_CS)[:16]
        return sha_half + manifold_half

    def hexdigest(self) -> str:
        return self.digest().hex()

    def phi_fingerprint(self) -> float:
        return self._phi

    def winding_class(self) -> int:
        return self._winding

    def is_similar_to(self, other: "GeometricHash", tol: float = 0.05) -> bool:
        """Return True if two hashes are geometrically proximate.

        Proximity is defined as |φ_a − φ_b| < tol AND same winding class.
        This enables *fuzzy de-duplication* on the manifold.
        """
        return (
            abs(self._phi - other._phi) < tol
            and self._winding == other._winding
        )

    @staticmethod
    def _compute_phi(data: bytes) -> float:
        arr = np.frombuffer(data, dtype=np.uint8).astype(np.int64) if data else np.zeros(1, dtype=np.int64)
        return float(int(np.sum(arr ** 2)) % K_CS) / K_CS

    @staticmethod
    def _compute_winding(data: bytes) -> int:
        digest = hashlib.sha256(data).digest()
        val = int.from_bytes(digest[:4], "big")
        return val % WINDING_NUMBER


# ===========================================================================
# ManifoldKey
# ===========================================================================

@dataclass
class ManifoldKey:
    """A public/private key pair derived from the manifold geometry.

    The **private key** is a WINDING_NUMBER-dimensional radion trajectory
    (a numpy float64 array seeded from the master secret).

    The **public key** is the winding-number projection of that trajectory:
        pub[i] = cos(priv[i] × 2π × n_w)   for i in range(4)
        pub[4] = Σ priv[i] mod K_CS / K_CS  (manifold fingerprint)

    Parameters
    ----------
    seed : bytes
        Master secret (at least 32 bytes recommended).
    tier : int
        Security tier (0 = kernel, 4 = sandbox).
    key_id : str
        Human-readable identifier.
    """
    seed: bytes
    tier: int = 3
    key_id: str = ""

    def __post_init__(self) -> None:
        if not 0 <= self.tier < WINDING_NUMBER:
            raise ValueError(f"Tier must be in [0, {WINDING_NUMBER}); got {self.tier}.")
        self._private = self._derive_private()
        self._public = self._derive_public()

    def _derive_private(self) -> np.ndarray:
        """Derive the private key vector from the seed."""
        rng = np.random.default_rng(
            seed=int.from_bytes(hashlib.sha256(self.seed).digest()[:8], "big")
        )
        return rng.uniform(0.0, 2.0 * np.pi, size=KEY_DIMENSION)

    def _derive_public(self) -> np.ndarray:
        """Derive the public key from the private key."""
        pub = np.empty(KEY_DIMENSION)
        for i in range(KEY_DIMENSION - 1):
            pub[i] = np.cos(self._private[i] * WINDING_NUMBER)
        # 5th component: manifold fingerprint of the private key bytes
        priv_bytes = self._private.tobytes()
        arr = np.frombuffer(priv_bytes, dtype=np.uint8).astype(np.int64)
        pub[KEY_DIMENSION - 1] = float(int(np.sum(arr ** 2)) % K_CS) / K_CS
        return pub

    @property
    def private_key(self) -> np.ndarray:
        """Return a copy of the private key vector."""
        return self._private.copy()

    @property
    def public_key(self) -> np.ndarray:
        """Return a copy of the public key vector."""
        return self._public.copy()

    def public_key_bytes(self) -> bytes:
        """Serialise the public key to bytes."""
        return self._public.tobytes()

    def __repr__(self) -> str:
        return (
            f"ManifoldKey(key_id={self.key_id!r}, tier={self.tier}, "
            f"pub={self._public.round(3)})"
        )


