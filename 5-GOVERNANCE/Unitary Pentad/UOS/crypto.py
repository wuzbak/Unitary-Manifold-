# ManifoldCipher
# ===========================================================================

class ManifoldCipher:
    """Symmetric stream cipher based on the manifold gauge field.

    The keystream is generated by evolving the radion field (φ) forward in
    manifold time:
        φ_{t+1} = φ_t − α × (φ_t − φ₀) × dt
    The keystream bytes are derived from the fractional part of φ at each
    step, scaled to [0, 255].

    Encryption:  ciphertext[i] = plaintext[i] XOR keystream[i]
    Decryption:  plaintext[i]  = ciphertext[i] XOR keystream[i]   (symmetric)

    Parameters
    ----------
    key : ManifoldKey

    Examples
    --------
    >>> cipher = ManifoldCipher(ManifoldKey(seed=b"my-secret"))
    >>> ct = cipher.encrypt(b"hello manifold")
    >>> cipher.decrypt(ct)
    b'hello manifold'
    """

    def __init__(self, key: ManifoldKey) -> None:
        self.key = key
        self._alpha = 0.05 + 0.01 * key.tier    # damping coefficient per tier

    def _keystream(self, length: int) -> bytes:
        """Generate ``length`` bytes of keystream."""
        # Seed φ from the private key mean
        phi = float(np.mean(self.key.private_key))
        dt = BRAIDED_SOUND_SPEED
        stream = bytearray(length)
        for i in range(length):
            phi = phi - self._alpha * (phi - PHI_BACKGROUND) * dt
            stream[i] = int(abs(phi) * 255) % 256
        return bytes(stream)

    def encrypt(self, plaintext: bytes) -> bytes:
        """Encrypt plaintext; return ciphertext."""
        ks = self._keystream(len(plaintext))
        return bytes(a ^ b for a, b in zip(plaintext, ks))

    def decrypt(self, ciphertext: bytes) -> bytes:
        """Decrypt ciphertext; return plaintext (symmetric with encrypt)."""
        return self.encrypt(ciphertext)  # XOR is its own inverse


# ===========================================================================
# CryptoEngine
# ===========================================================================

class CryptoEngine:
    """Orchestrates all UOS cryptographic primitives.

    Manages a key store, provides convenience methods for hashing,
    signing, verification, and encryption.

    Examples
    --------
    >>> engine = CryptoEngine()
    >>> kid = engine.generate_key(seed=b"seed", tier=2, key_id="alice")
    >>> sig = engine.sign(b"data", kid)
    >>> engine.verify(b"data", sig, kid)
    True
    >>> ct = engine.encrypt(b"secret", kid)
    >>> engine.decrypt(ct, kid)
    b'secret'
    """

    def __init__(self) -> None:
        self._keys: Dict[str, ManifoldKey] = {}

    def generate_key(
        self, seed: bytes, tier: int = 3, key_id: str = ""
    ) -> str:
        """Generate and store a new ManifoldKey.

        Returns
        -------
        str
            The key_id used to reference this key.
        """
        if not key_id:
            key_id = GeometricHash(seed).hexdigest()[:16]
        key = ManifoldKey(seed=seed, tier=tier, key_id=key_id)
        self._keys[key_id] = key
        return key_id

    def get_key(self, key_id: str) -> ManifoldKey:
        if key_id not in self._keys:
            raise KeyError(f"Key '{key_id}' not found in CryptoEngine.")
        return self._keys[key_id]

    def hash(self, data: bytes) -> GeometricHash:
        """Return a GeometricHash of ``data``."""
        return GeometricHash(data)

    def sign(self, message: bytes, key_id: str) -> ManifoldSignature:
        """Sign ``message`` with the key identified by ``key_id``."""
        return ManifoldSignature(message, self.get_key(key_id))

    def verify(
        self, message: bytes, sig: ManifoldSignature, key_id: str
    ) -> bool:
        """Verify a signature."""
        return sig.verify(message, self.get_key(key_id))

    def encrypt(self, plaintext: bytes, key_id: str) -> bytes:
        """Encrypt plaintext with the manifold cipher."""
        return ManifoldCipher(self.get_key(key_id)).encrypt(plaintext)

    def decrypt(self, ciphertext: bytes, key_id: str) -> bytes:
        """Decrypt ciphertext with the manifold cipher."""
        return ManifoldCipher(self.get_key(key_id)).decrypt(ciphertext)

    def list_keys(self) -> List[str]:
        """Return a sorted list of all key IDs."""
        return sorted(self._keys.keys())

    def stats(self) -> Dict:
        return {
            "keys_stored": len(self._keys),
            "key_ids": self.list_keys(),
        }
