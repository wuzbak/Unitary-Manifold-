# ManifoldSignature
# ===========================================================================

class ManifoldSignature:
    """Digital signature using the manifold geometry.

    Signing:
        sig = HMAC-SHA256(private_key_bytes, message) XOR manifold_projection

    Verification:
        Recompute the expected HMAC, XOR with the manifold projection, and
        compare.  Also verify the geometric invariant holds for the message.

    Parameters
    ----------
    message : bytes
    key : ManifoldKey

    Examples
    --------
    >>> key = ManifoldKey(seed=b"secret", tier=2)
    >>> sig = ManifoldSignature(b"important message", key)
    >>> sig.verify(b"important message", key)
    True
    >>> sig.verify(b"tampered message", key)
    False
    """

    def __init__(self, message: bytes, key: ManifoldKey) -> None:
        self.message = message
        self.key_id = key.key_id
        self._sig_bytes = self._sign(message, key)

    def _sign(self, message: bytes, key: ManifoldKey) -> bytes:
        # HMAC-SHA256 base
        priv_bytes = key.private_key.tobytes()
        h = hmac.new(priv_bytes, message, hashlib.sha256).digest()
        # XOR with manifold projection (first 32 bytes of public key bytes × 2)
        pub_bytes = (key.public_key_bytes() * 4)[:32]
        pub_arr = np.frombuffer(pub_bytes, dtype=np.uint8)
        h_arr = np.frombuffer(h, dtype=np.uint8)
        xored = (h_arr ^ pub_arr).tobytes()
        return xored

    @property
    def signature_bytes(self) -> bytes:
        return self._sig_bytes

    def verify(self, message: bytes, key: ManifoldKey) -> bool:
        """Return True if ``message`` was signed by ``key``."""
        expected = self._sign(message, key)
        return hmac.compare_digest(expected, self._sig_bytes)

    def geometric_invariant_ok(self) -> bool:
        """Return True if the signed message satisfies the φ-invariant."""
        gh = GeometricHash(self.message)
        # Valid signatures are on the manifold (phi in a reasonable range)
        return 0.0 <= gh.phi_fingerprint() < 1.0


